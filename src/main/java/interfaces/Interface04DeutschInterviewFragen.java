    /*

Frage 1: Was ist ein Java-Interface?
Antwort: Ein Java-Interface ist eine Sammlung von abstrakten Methoden, die von Klassen implementiert werden können.
Es dient als Vertrag oder Schnittstelle, die sicherstellt, dass implementierende Klassen bestimmte Methoden
zur Verfügung stellen.

Frage 2: Wie definiert man ein Interface in Java?
Antwort: Ein Interface wird mit dem Schlüsselwort interface definiert.

Frage 3: Was ist der Unterschied zwischen einem Interface und einer abstrakten Klasse in Java?
Antwort: Der Hauptunterschied besteht darin, dass ein Interface keine Implementierung von Methoden enthält,
während eine abstrakte Klasse sowohl abstrakte als auch konkrete Methoden enthalten kann.
Eine Klasse kann von mehreren Interfaces erben, aber nur von einer abstrakten Klasse.

Frage 4: Kann ein Interface Felder enthalten?
Antwort: Ja, Interfaces können Konstanten (final und static Felder) enthalten.
Diese Felder sind implizit public, static und final, und ihre Werte können nicht verändert werden.

Frage 5: Wie implementiert man ein Interface in einer Klasse?
Antwort: Um ein Interface in einer Klasse zu implementieren, verwendet man das Schlüsselwort implements.

Frage 6: Kann eine Klasse mehrere Interfaces implementieren?
Antwort: Ja, eine Klasse kann mehrere Interfaces implementieren,
indem sie sie durch Komma getrennt in der implements-Klausel angibt.

Frage 7: Kann ein Interface ein anderes Interface erweitern?
Antwort: Ja, ein Interface kann ein anderes Interface mithilfe des Schlüsselworts extends erweitern.

? Frage 9: Was ist das Problem des Diamant-Erbschaftsproblems in Bezug auf Interfaces?
Antwort: Das Diamant-Erbschaftsproblem tritt auf, wenn eine Klasse von zwei Interfaces erbt,
die eine Methode mit demselben Namen und derselben Signatur haben. In solchen Fällen muss die abgeleitete Klasse die Methode überschreiben und die gewünschte Implementierung bereitstellen.

Frage 10: Warum verwendet man Interfaces in Java?
Antwort: Interfaces werden in Java verwendet, um abstrakte Verträge zwischen Klassen herzustellen,
um sicherzustellen, dass bestimmte Methoden vorhanden sind.
Sie ermöglichen auch die Implementierung von Mehrfachvererbung und Polymorphismus.


     */
