1. Was ist ein Konstruktor in Java?

Ein Konstruktor ist eine spezielle Methode in einer Java-Klasse, die aufgerufen wird,
wenn ein Objekt dieser Klasse erstellt wird.
Er initialisiert die Eigenschaften des Objekts und führt andere Aufgaben aus,
die beim Erstellen des Objekts erforderlich sind.

2. Welche Arten von Konstruktoren gibt es in Java?

In Java gibt es zwei Hauptarten von Konstruktoren:
Default-Konstruktor: Wenn Sie keinen Konstruktor in Ihrer Klasse definieren,
wird automatisch ein Standardkonstruktor ohne Parameter erstellt.
Benutzerdefinierter Konstruktor: Sie können eigene Konstruktoren erstellen,
um Objekte mit bestimmten Werten zu initialisieren.

3. Wie unterscheidet sich ein Konstruktor von einer normalen Methode?

Konstruktoren haben denselben Namen wie die Klasse selbst, während Methoden unterschiedliche Namen haben können.
Konstruktoren werden automatisch aufgerufen, wenn ein Objekt erstellt wird,
während Methoden explizit aufgerufen werden müssen.
Konstruktoren haben keinen Rückgabetyp, nicht einmal void.

5. Können Sie mehrere Konstruktoren in einer Klasse haben?

Ja, Sie können mehrere Konstruktoren in einer Klasse haben,
solange sie sich in der Anzahl oder Art der Parameter unterscheiden.
Dies wird als Überladen von Konstruktoren bezeichnet.

7. Was ist ein Parameterkonstruktor?

Ein Parameterkonstruktor ist ein benutzerdefinierter Konstruktor, der Parameter akzeptiert,
um die Objekteigenschaften während der Initialisierung festzulegen.

9. Was ist der Zweck eines Standardkonstruktors in Java?

Der Standardkonstruktor dient dazu,ein Objekt zu erstellen und dessen Eigenschaften mit Standardwerten zu initialisieren,
wenn kein benutzerdefinierter Konstruktor vorhanden ist.




