package mathemathicalexpressions;

public class Math01 {
    public static void main(String[] args) {
        System.out.println(14);//14
        System.out.println(9);//9
        double sonuc = 9.0;
        System.out.println(sonuc);//9.0
        sonuc = 9.0;
        System.out.println(sonuc);//9.0
        sonuc = 9.333333333333334;
        System.out.println(sonuc);//9.333333333333334
        sonuc = 9.333333333333334;
        System.out.println(sonuc);//9.333333333333334;
        int sayi1 = 5;
        int sayi2 = 2;
        double sayi3 = 2.0;
        System.out.println(sayi1 / sayi2);//2
        System.out.println((double)sayi1 / sayi3);//2.5
        System.out.println((double)sayi1 + sayi3 / (double)sayi2);//6.0

    }
}
