package scanner;

public class ScannerDeutschInterviewFragen {
    /*
1.Was ist die Scanner-Klasse in Java?
Antwort:Benutzereingaben und andere Daten aus verschiedenen Quellen zu lesen.

2.Wie importieren Sie die Scanner-Klasse in einem Java-Programm?
Antwort: Sie importieren die Scanner-Klasse import java.util.Scanner

3.Wie erstellen Sie ein Scanner-Objekt, um Eingaben von der Konsole zu lesen?
Antwort: Sie erstellen ein Scanner-Objekt,
indem Sie Scanner scanner = new Scanner(System.in); verwenden.

4.Wie liest man eine ganze Zahl (z.B. int) von der Konsole mit dem Scanner?
Antwort: Sie können int num = scanner.nextInt(); verwenden,
um eine ganze Zahl von der Konsole einzulesen.

5.Wie liest man eine Gleitkommazahl (z.B. double) von der Konsole mit dem Scanner?
Antwort: Sie können double num = scanner.nextDouble(); verwenden, um eine Gleitkommazahl von der Konsole einzulesen.

6.Wie liest man einen Zeichen (z.B. char) von der Konsole mit dem Scanner?
Antwort: Sie können char ch = scanner.next().charAt(0); verwenden, um ein Zeichen von der Konsole einzulesen.

7.Wie liest man einen String von der Konsole mit dem Scanner?
Antwort: Sie können String str = scanner.next(); verwenden,
um einen einzelnen Wort-String oder
String str = scanner.nextLine(); für eine ganze Zeile einzulesen.

8.Wie überprüfen Sie, ob es noch Eingaben in der Konsole gibt, bevor Sie mit dem Scanner lesen?
Antwort: Sie können hasNext() oder hasNextLine() verwenden, um zu überprüfen,
ob noch Eingaben vorhanden sind, bevor Sie mit dem Scanner lesen.

9.Wie schließen Sie das Scanner-Objekt ordnungsgemäß, wenn Sie es nicht mehr benötigen?
Antwort: Sie sollten das Scanner-Objekt mit scanner.close();
schließen, um Speicherlecks oder Ressourcenlecks zu vermeiden,
wenn Sie es nicht mehr benötigen.
     */
}
